// Helper function to get reciter image by name

export function getReciterImage(reciterName: string, fallbackImage: string) {
  // Normalize the name by trimming whitespace
  const normalizedName = reciterName?.trim();

  if (!normalizedName) {
    return fallbackImage;
  }

  // Direct lookup
  if (reciterImageMap[normalizedName]) {
    return reciterImageMap[normalizedName];
  }

  // Case-insensitive lookup for English names
  const lowerCaseName = normalizedName.toLowerCase();
  const foundKey = Object.keys(reciterImageMap).find(
    (key) => key.toLowerCase() === lowerCaseName
  );

  return foundKey ? reciterImageMap[foundKey] : fallbackImage;
}

export function getReciterRadio(reciterName: string, radioData: Radio[]) {
  // Normalize the name by trimming whitespace
  const normalizedName = reciterName?.trim().toLowerCase();
  const foundKey = radioData.find((radio) =>
    radio.name.toLowerCase().includes(normalizedName)
  );
  return foundKey;
}

interface AvailableRecitationTypesProps {
  reciter: Reciter;
  recitationTypes: {
    murattal: string;
    mojawwad: string;
    "mo'lim": string;
  };
}
export function getAvailableRecitationTypes({
  reciter,
  recitationTypes,
}: AvailableRecitationTypesProps): string[] {
  if (!reciter.moshaf?.length) return [];

  const availableTypes: string[] = [];

  // Check for each recitation type
  Object.entries(recitationTypes).forEach(([key, translatedType]) => {
    const hasType = reciter.moshaf?.some(
      (moshaf) =>
        moshaf.name.toLowerCase().includes(key.toLowerCase()) ||
        moshaf.name.toLowerCase().includes(translatedType.toLowerCase())
    );

    if (hasType) {
      availableTypes.push(translatedType);
    }
  });

  return availableTypes;
}
